public class EvenOdd {
    public static void main(String[] args) {

        //Variables
        int numToExamine = 2;

        // If statement
        if (numToExamine % 2 == 0)
            System.out.println(numToExamine + " Is an Even Number");
        else
            System.out.println(numToExamine + " Is and Odd Number");

    }
}
