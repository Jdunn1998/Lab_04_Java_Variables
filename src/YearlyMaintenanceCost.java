public class YearlyMaintenanceCost  {
    public static void main(String[] args) {

        //Variables
        double springCost = 190.00;
        double summerCost = 210;
        double fallCost = 175;
        double winterCost = 250;
        double totalCost = springCost + summerCost + fallCost + winterCost;

        //Outputs
        System.out.println("The Spring Maintenance is: $" + springCost);
        System.out.println("The Summer Maintenance is: $" + summerCost);
        System.out.println("The Fall Maintenance is: $" + fallCost);
        System.out.println("The Winter Maintence is: $" + winterCost);
        System.out.println("The Total Yearly Maintence is: $" + totalCost);

    }
}
